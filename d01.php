        <h4>第1天: 加德满都 – 卢卡拉 – 巴克丁</h4>
      </div>
      <p>
        我们按时到达机场，但被告知飞机晚点。这太正常不过了:
        卢卡拉机场没有塔台、没有辅降设备。跑道总长只有527米，宽20米，呈11.7度仰角
        (为着陆飞机减速)。这不是全球最短、但却是全球最高的一条民航跑道。其一端是深谷，另一端是山墙。当飞机着陆时，如触地过早会掉下深谷，若过晚
        (或刹车失灵)
        会撞上跑道末端的山墙。飞机只要开始降落就必须完成，没有重飞的可能,
        即使在好的天气下该机场也只有几小时的窗口飞机可以起降。风切变、强劲的山风、甚至山里飘来的云雾和其它很多因素都会影响飞行安全。虽然卢卡拉离加德满都飞行距离只有半小时，但很多人宁愿走五天山路从加德满都到卢卡拉也不坐飞机。我们是因为时间不够才选乘飞机的。<br /><br />
        我们乘坐的是加拿大产的DHC-8飞机，有20来个座位，是唯一可降落在像卢卡拉机场的短跑道的固定机翼民航机。延误两小时后我们起飞了。20来分钟左右可见飞机两边都是高山峭壁，可见我们已经进入山谷降落模式。飞机颠簸越来越厉害,
        云雾也在不断加厚。据说上次空难是因为飞机下降时山口突然飘来几朵云彩遮挡了飞行员的视线，使飞机触地过早撞上跑道前的峭壁…
        就在心都跳到嗓子口时，咣当一声飞机轮胎触地。随后便是急刹车，飞机向右急拐后终于停稳。不少乘客一下飞机就直奔厕所。<br /><br />
        我们取出背包后便穿过卢卡拉小镇，正式开始了前往珠峰大本营的徒步。<br /><br />
        出镇后的山路上上下下，缭绕山脉河流。路上我们遇到一些徒步者，更多背负重物的搬运工和牲畜搬运输队。这一天看的主要不是巍峨的大雪山，但泛着浪花的溪流以及盘旋在绿荫中的小路还是令人心旷神怡。路上一家面对高山峡谷的饭店吃了午饭。走了4、5个小时后终于到达了徒步的第1站巴克丁。<br /><br />
        头一天背15公斤重的背包累的腰酸背痛。我们随便找了家外表看来较新的茶屋，问了问价钱(500卢比单间，美元兑换率是130)便入住了。后来才知道山上所有茶屋均为每人500卢比一晚
        (如茶屋满了你要单人单间也许会1000卢比)。吃饭和饮料及其它费用往往高于住宿费。许多茶屋都要求你不到外面吃饭,
        否则将收你两到三倍的住宿费。<br /><br />
        各茶屋的菜单也是大同小异，价格相当，只不过海拔越高越贵。这家茶屋的菜单包括:
        尼泊尔定食Dal Bhat (大盘子里分别盛有煮豆、米饭、小蝶蔬菜、罗蒂饼)，
        各式素食菜、煮/炒蛋，烤牛排、炖鸡猪羊肉、茶、西方的意大利面、比萨、方便面，
        面包、咖啡、饮料… 菜单足有几页纸长。<br /><br />
        晚饭后我们与店主聊了一会儿天。男主人曾九次登上珠峰，攒了一些钱，便开始开茶屋
        – 这是许多夏尔巴人致富的途径。因室内温度急剧下降
        (就我们两个客人，我的伙伴告诉他们不用专门生火供我们取暖)，我们撤回到各自的房间钻入睡袋取暖，Wi-Fi网速慢，这里也没有手机信号。所以这一天我们不仅经历了山路的艰辛外也品尝了此后不断袭扰我们的茶屋无暖气之苦。
      </p>
