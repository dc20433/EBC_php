      <p>
        昨晚吃晚饭时就被告知今天要早起：6点半吃早饭，7点出发前往格拉克樰陂 -- 本次旅途的最后一个村庄。因为珠峰大本营没有住宿设施，徒步者到达格拉克樰陂后，选好茶屋，放下背包，便前往3.5公里外的珠峰大本营，再按原路返回格拉克樰陂过夜。<br /><br />
        我又雇了搬运工背包，顶着风雪，不时用围巾挡住鼻子过滤冷气，不到两小时就抵达格拉克樰陂。我的伙伴昨天就感到不舒服
        (或许是高山反应)，但还是坚持自己扛包，不用说他又被我甩在后面。我在格拉克樰陂茶屋等了他半天他才到达。此时已过午，风雪大大加剧。我知他知他是没法再走了，我推开大门独自步入了风雪漫天的白色世界。虽然雪打的脸发麻，但我惊讶的发现不少同我思维一致的人也挣扎在风雪中奔向珠峰大本营。我们的想法大致这样：若今天不去珠峰大本营，明天就不能下山，那就要在这冰窟里多呆一晚。但如果明天急着下山不去珠峰大本营了，那这趟苦就等于白受了。<br /><br />
        前往珠峰大本营的道路坑坑洼洼，大雪覆盖着岩石，徒步者、搬运工
        、牲口搬运队都挤在这条狭路上。牲口把躲避不当的人挤掉悬崖的事不是没发生过，但那时谁也顾不上了。还是有很多搬运工背着沉重的货物，甚至建材、机械、家具，还有一部浓缩咖啡机，在风雪中奔往珠峰大本营。<br /><br />
        我又忙着不断的把围巾拉到脸上用来过滤吸入鼻中的冷气。也不知挣扎了多久，突然眼前一亮，远处拐角隐隐约约出现了不少人影和一些橙色的帐篷。珠峰大本营啊，可谓历经千辛万险，我终于来到了你的身边！相信那一刻我激动的落泪了。<br /><br />
        走近才发现这里除了一块岩石刻着红色的大字"珠峰大本营"、一个1953年攀登珠峰的先驱者希拉里爵士和
        Sherpa Tenzing
        Norgay的宣传画，远处有一些树立在风雪中的帐篷外，这个全球最著名的营地实际上平淡无奇。这里没有一个象征进入营地的大门，没有可供游人休息的咖啡厅洗手间，没有任何设施。我打开手机，准备不管家人朋友们是什么时间也要同进行视频通话，但惊讶的发现这里手机没有信号。<br /><br />
        我在石碑画板前留影拍照。当时一群人正在录制节目，其中一个年轻人正对着话筒激动的说，“这次探险尼泊尔同事们传经送宝，我向他们学到了很多珍贵的东西…
        看架势此人说不准是刚从8848.86米的珠峰顶下来的。这使我意识到对很多人来说到达珠峰大本营也许是一件微不足道的小事一桩…<br /><br />
        这里停留实在太冷。被折磨了15、20分钟后我只好启程返航。此时已不早，但途中居然还碰上许多正在风雪中奔往珠峰大本营人，有些是我在路上认识的。他们祝贺我已经到达了此次旅行的终点。<br /><br />
        不用说这一晚又冻得够呛。数天徒步积累的辛劳使我感到分外疲劳。我真希望明天没有能见度，给我个理由直接下山，不用再爬到5643米的Kalapattar峰顶去近看珠峰了。<br /><br />
      </p>
