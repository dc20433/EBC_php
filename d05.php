        我们在路上碰上了一个单行的中国女孩，来自广州。她刚从珠峰大本营下来，我们都羡慕极了。她告诉我们越往上会越难，还专门提到了到达罗布崅 (最后的第2站) 要翻越一堵墙。但她丝毫不怀疑我们一定能达到终点，因为她就算活生生的证明。<br /><br />
        这一天爬上爬下走得也很辛苦，丁布崅在海拔4500米，比一天前海拔净增500米。这里有很多整齐划一、为登珠峰的人设的帐篷营地，让他们开始适应帐篷生活。据说他们要住在这样的帐篷里训练数月才能开始登顶。<br /><br />
        丁布崅是我们高原适应性调整的第2站，明天不用扛大背包行走了。
