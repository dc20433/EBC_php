      <p>
        这一天路程并不远，但走得辛苦，因为要爬太多的陡坡，还要背着沉重的背包。可以看到遥远白顶山，景色比前一天更为壮观和惊险。这里还可以看到很多农田，路上很多学童。一条湍急的大川伴随着弯弯曲曲的山路，徒步者要跨过这条大川数次。这些架设在深不见底的大川上的铁索桥让峡谷中呼啸的强烈山风吹动下摇摇晃晃，令过桥的人胆战心惊。我每次过桥时都目不斜视，全神贯注前面那个人的背包来抵消恐高感，因此无法拍下高空行走的惊险镜头。曾看到一位徒步者犹犹豫豫就是不敢上桥，最后不知她的结局如何。<br /><br />
        今天下一半的路途，有4公里长，是爬陡坡。途中不断有背负重物的搬运工和牦牛大队超越我们。我的伙伴是位能吃苦的徒步者，从小就喜好这项运动。我们在过去的十来年中几乎每年都相约长途徒步一次。这是他第三次徒步去珠峰大本营，而我是第一次。但不知为何他今天步步挣扎，使我开始担心如果他撑不住了怎么办。我只好走走停停，边走边等。等到达南崅集市时天色已晚。幸亏他来过这里，我们很快入住了他熟悉的茶屋。<br /><br />
        南崅集市建在山坳中一座大山的陡坡上，面对几座更大的高山，景色独特宏伟壮观，令人眼目一新。她是整个孔布地区(Khumbu，尼泊尔珠峰国家公园所在地)
        的集散地、也是除了直升机以外进出山的唯一通道，几条进山的道路都在此汇合。每周六赶一次集，全区的人都到这来做交易。这里有6、70个茶屋。这是进山的最后一站可以洗付费热水澡、给手机免费充电、网络信号足够上网。再往上走这些都变成了奢侈品。<br /><br />
        美国丹佛以一英里高海拔城市著称，很多人到达后会出现高山反应，接不上气或头晕。而南崅集市在海拔3440米，是两英里高的镇子，去珠峰大本营的徒步者都被告知要在这停留一天，做高原适应性调整。我们在此住了两晚。<br /><br />
