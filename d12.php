        早上5点来钟就可以听到很多房客开始起床整装。因为谁都知道如果今天机场开放，座位一定很紧张，人人都抢先赶去机场。我也起床整好背包，结账，没吃早饭就直奔机场。<br /><br />
        清晨云开雾散，候机厅到处都是人。人们把发登机卡的柜台围的水泄不通。有人说今天会有8、9班放飞，但没有任何来自官方的正式消息。我看到一位尼泊尔白发长者，工作人员对他很敬重、认同。他脸上总带着微笑，显得很和蔼。每当他从我身边经过时我都与他拉话头。我问他这样的挑战是独特的还是常有的？他说既独特又经常。我问他有没有机场空闲到没有乘客的时候，这个问题他没有答。我看到三个女孩跟他诉苦，说她们已经在这里等了两天了，其中一个说着说着眼泪就掉下来了：“…
        我们不能再回那寒冷的茶屋再过一夜…"
        当他又从我身旁走过时，我打趣的说管理客户的眼泪也是你的工作职责了。他开始注意到我了。<br /><br />
        前六架飞机都满载，座位可能全让大旅游团包下了。看着机场还有那么多人，而我的票是几天后的，没有登机优先权。我开始考虑一个向导的提议，他安慰我说如果8点半他们还上不了飞机，他就带他的客户们徒步下山，走一天远路后再乘两天吉普车赶回加德满都。我可以跟他们一起行动。(有人跟我说一天的越野吉普车下来全身骨头像散架了一样)。就在我感到绝望的时候，第7架飞机降落了。这位尼泊尔长者把我护照递给办票员，她二话不说就填了登记卡。他給我解释说今天没有去加德满都的航班，明天机场能不能起降还很难说，所有他把我办到加德满都的备降机场冉梅楂岥，从那里我可以乘五小时的机场大巴回加德满都。看着大厅站得满满的人在等登记卡，我对站长感激不尽。他勉强听完我的感激，给我一个微笑，便又去忙他的事了。<br /><br />
        半小时后我登上了今天的第7架飞机，终于出山了。昨晚还遥不可及的加德满都，我回来了！<br />
