      <p>
        大雪下了一夜，早晨还在下，所以没人再谈攀登Kalapattar峰的事了，纷纷准备下山。我的伙伴也决定走，但仍然拒绝雇人背他的大背包。他将无法跟上其他人的步伐。我认为请挑夫一方面不致把把人累垮，另一方面为当地经济做贡献，何乐而不为。但他不听。直到最后一波人马上也要离开，他让我跟他们走。他们已经雇了人为他们背包，准备轻装快步前行，走的越远越好;&nbsp我的伙伴则自己扛包慢慢下山，能走多远算多远(他比我晚三天回到加德满都)。<br /><br />
        这帮青年人因下山心切，在雪地里基本上是一路小跑。我勉强可以跟上他们的步伐。虽下山走的是回头路
        (除了吐卡拉与谭布崅之间的一小段)，但因为方向相反，加上大雪装点，看到的是全新景色，倒像走一条新路线。看来这个回头路值得走。<br /><br />
        我们第一天在暴风雪中奔跑了近15公里，下到海拔4000米的潘布崅。在那里又挨了一夜冻。第二天雪停了，蔚蓝的天空衬托着白色的高山，凸显喜马拉雅山的独特美景。路过白雪皑皑的谭布崅时又见了一回珠峰，
        还是那样的妖娆。不知有没有人后悔在格拉克樰陂没去爬Kalapattar。<br /><br />
        我们又一口气跑了14公里，穿越南崅集市，直接下到海拔2800米的孟秋。全亏有熟悉情况的向导，把我们带到一个叫Summit的茶屋，给我们留下了美好的记忆。这间茶屋的卧室居然带有洗手间。更想不到的是洗手间居然有热水！这是离开加德满都后10天来第一次洗了个像样的热水澡。晚饭相聚时人人兴高采烈，像重获新生一样。这也是进山以来我们都第一次喝酒，庆贺了一番。晚上房间温度还是零下，但洗了个痛快的热水澡，明天就是在山上的最后一晚了，相信每个人都睡得很甜。<br /><br />
        次日我们走不到四个小时便到达了徒步下山的最后一站卢卡拉。谁知在这里等待着我们的是机场因天气已经关闭了两天的坏消息。卢卡拉本来就不大，沉淀了三天的徒步者把所有茶屋都挤得满满的。不仅明天能否回加德满都成了疑问，今晚住宿都成了问题。我在路上认识的一个向导朋友花了不少力气为我找到了一间客房，不然那晚我只能在人家的饭厅里打通铺。<br /><br />
        又是一个寒夜，但最糟糕的是明天回程不定，不知要在这冰天雪地里呆多久，我整夜无法入睡。
      </p>
